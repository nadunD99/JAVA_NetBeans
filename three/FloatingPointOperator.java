public class FloatingPointOperator
{
	public static void main(String[] args)
	{
	System.out.println("*************************\n ");
 	System.out.println(" (12.0 + 49.2) = "+ (12.0 + 49.2));
 	System.out.println(" (12.3 + 49.2) = "+ (12.3 + 49.2));
 	System.out.println(" (12.3 - 49.2) = "+ (12.3 - 49.2));
 	System.out.println(" (12.3 * 49.2) = "+ (12.3 * 49.2));
 	System.out.println(" (12.3 / 49.2) = "+ (12.3 / 49.2));
	
	System.out.println("\n************************* ");

	}

}